numbs = [57.8, 46.51, 97, 10, 20.14, 30.18, 40.05, 50.98, 9077, 1] #Список чисел float / int
for i in numbs:
    rub = int(i)    # Все число приводим в int
    kk = (i - int(i)) * 100  # Копейки = 50.00(float)-50(itn) = .00 * 100 ( вывод в целое число)
    print(f'{rub} руб {kk:02.0f} коп', end=', ')
